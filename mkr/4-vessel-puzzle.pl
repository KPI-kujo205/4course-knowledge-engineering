% Завдання 4: Логічна задача про стародавню посудину
%
% Умова:
%   Альоша: "Це грецька посудина" (A1) І "виготовлена в V столітті" (A2)
%   Боря:   "Це фінікійська посудина" (B1) І "виготовлена в III столітті" (B2)
%   Гриша:  "Це не грецька посудина" (G1) І "виготовлена в IV столітті" (G2)
%   Кожен правий рівно в одному з двох припущень.

% xor/2 — рівно одне з двох тверджень істинне
xor(true, false).
xor(false, true).

% bool/2 — перетворює результат умови у true/false
bool(Goal, true)  :- call(Goal), !.
bool(_,    false).

% solve/2 — знаходить походження посудини та вік її виготовлення
solve(Origin, Century) :-
    % Перебираємо можливі значення походження
    member(Origin, [greek, phoenician, other]),
    % Перебираємо можливі значення століття
    member(Century, [3, 4, 5]),

    % --- Припущення Альоші ---
    % A1: посудина грецька
    bool(Origin = greek, A1),
    % A2: виготовлена в V столітті
    bool(Century = 5,    A2),
    % Альоша правий рівно в одному
    xor(A1, A2),

    % --- Припущення Борі ---
    % B1: посудина фінікійська
    bool(Origin = phoenician, B1),
    % B2: виготовлена в III столітті
    bool(Century = 3,         B2),
    % Боря правий рівно в одному
    xor(B1, B2),

    % --- Припущення Гриші ---
    % G1: посудина не грецька
    bool(Origin \= greek, G1),
    % G2: виготовлена в IV столітті
    bool(Century = 4,     G2),
    % Гриша правий рівно в одному
    xor(G1, G2).

% Головний предикат — виводить результат
main :-
    ( solve(Origin, Century) ->
        format("Посудина: ~w~n", [Origin]),
        format("Вік виготовлення: ~w столiття~n", [Century])
    ;
        write("Рiшення не знайдено.")
    ).

:- initialization(main).
